# Q3 WAP using variables to find the profit and profit percentage of a certain transaction where S.P = $10000 and C.P $7000

selling_price = 10000
cost_price = 1000

# calculate profit
profit = selling_price - cost_price

# calculate profit percentage
profit_percentage = (profit / cost_price) * 100 

print(f'the profit is {profit} and profit percentage is {profit_percentage}')